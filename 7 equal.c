#include<stdio.h>

int main()
{
    int number1 = 5, number2 = 2;

    if (number1 == number2)
    {
        printf(" Numbers are equal.\n");
    }
    else if (number1 > number2)
    {
        printf(" Number1 is greater than number2.\n");
    }
    else
    {
        printf(" Numbers are not equal.\n");
    }
    return 0;
}
